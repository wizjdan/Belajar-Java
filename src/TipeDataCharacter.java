public class TipeDataCharacter {
    public static void main(String[] args) {
        /*
        Untuk membuat tipedata character di Java, kita bisa menggunakan tanda ' (petik satu) diawal dan di akhir karakter
         */
        char n = 'N';
        char u = 'U';
        char g = 'G';
        char i = 'I';

        System.out.print(n);
        System.out.print(u);
        System.out.print(g);
        System.out.print(i);
    }
}
